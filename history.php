		<h3>Historia da Empresa</h3>
		
		<span>UMA GIGANTE VERDE E AMARELA</span><br><br>
	<p>A história da Granol começa com o Sr. José Gomes Cadette, com 19 anos de idade, um entusiasta da agricultura que entrou para a CIA Continental de Cereais (ContiBrasil), empresa americana que explorava a exportação e importação de produtos agrícolas em vários países e era proprietária de várias fábricas de óleos vegetais pelo mundo.<br></p>

	<p>Passados seis anos e uma intensa experiência vivida nesta empresa, o Sr. Gomes juntamente com o Sr. Germano Guido Baldi e o Sr. João Serdan D’Alarico, todos da ContiBrasil, iniciam o seu próprio negócio voltado para a prestação de serviços de comércio internacional, assessoria fiscal e financeira e contratação de fretes internos e marítimos para exportação dos produtos brasileiros, assim nascia a Granol em 1965.<br></p>

	<p>A Granol continuou a crescer com aquisições e, em 1975, o Dr. Juan Diego Pablo Ferrés, um Uruguaio, formado em engenharia, ingressou na Granol como um novo acionista. O Dr. Diego, era uma força motivadora forte na Granol para continuar a compra de outras plantas industriais e ajudar a Granol a se expandir para outros estados.<br></p>

	<p>Em 1998 foi a vez do Sr. João Serdan D’Alarico deixar a Granol e hoje os únicos 3 acionistas são o Sr. José Gomes Cadette, o Sr. Mario Hoshika e o Sr. Juan Diego Ferrés, que continuou a expansão da Granol com aquisições e com novas oportunidades no biodiesel, indústrias de glicerina e lecitina.<br></p>

	<p>Resultado: Hoje, com mais de meio século de tradição, a Granol se constitui em um dos maiores e mais respeitáveis complexos de agronegócios genuinamente brasileiro. São 5 complexos industriais, 24 regionais de compra e armazenagem de grãos, 3 usinas de biodiesel, 2 usinas de glicerina, 1 fábrica de lecitina, além do escritório matriz em São Paulo-SP.<br></p>

	<p>Soma-se a isso a capacidade estática de armazenamento superior a 925.500 tons de grãos e 246.180 tons de farelo, esmagamento de 3.399.000t/ano, 2.651.000t/ano de farelo de soja, refino de 600.600t/ano de óleo bruto e envase, produção de 1.074.000m³/ano de biodiesel, 66.000t/ano de glicerina bidestilada, 9.000t/ano de lecitina, 13.000 fornecedores e mais de 5.000 clientes ativos.<br></p>

	<p>A Granol se orgulha de ser brasileira. E faz tudo para que o Brasil se orgulhe dela também.<br></p>
